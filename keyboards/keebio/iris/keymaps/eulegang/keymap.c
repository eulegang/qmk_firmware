bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case QWERTY:
      if (record->event.pressed) {
        set_single_persistent_default_layer(LI_QWERTY);
      }
      return false;
      break;

    case SYM:
      if (record->event.pressed) {
          layer_on(LI_SYM);
      } else {
          layer_off(LI_SYM);
      }

      return false;
      break;

    case NUM:
      if (record->event.pressed) {
          layer_on(LI_NUM);
      } else {
          layer_off(LI_NUM);
      }

      return false;
      break;
  }
  return true;
}
